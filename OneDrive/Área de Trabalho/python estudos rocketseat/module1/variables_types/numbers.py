# Inteiro
numero_inteiro = 4521
print("Inteiro = ", numero_inteiro) 
# Exibe o valor da variável numero_inteiro (Float)


# Real com ponto flutuante (Float)
numero_real = 9.436
print("Real com ponto flutuante = ", numero_real)
# Exibe o valor da variável numero_real

# type() - função de retornar o tipo da variável
print("Tipo da variável Inteiro = ", type(numero_inteiro))
print("Tipo da variável Real = ", type(numero_real))

# SOMA +
soma = 1 + 1
print("1 + 1 = ", soma) # Exibe o valor da variável soma
print("O valor da soma é: ", soma) # Exibe o valor da variável soma

# SUBTRAÇÃO - 
subtracao = 2 - 1 
print("2 - 1 = ", subtracao) # Exibe o valor da variável subtracao
print("O valor da subtração é: ", subtracao)
# Exibe o valor da variável subtracao

# MULTIPLICAÇÃO *
multiplicacao = 2 * 5
print("2 * 5 = ", multiplicacao) # Exibe o valor da variável multiplicacao

print("O valor da multiplicação é: ", multiplicacao)
# Exibe o valor da variável multiplicacao